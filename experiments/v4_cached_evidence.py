def plot_bar(path: Path, labels: list[str], values: list[float], title: str, ylabel: str, color: str) -> None:
    fig, ax = plt.subplots(figsize=(8.0, 4.2), dpi=160)
    positions = range(len(labels))
    ax.bar(list(positions), values, color=color, edgecolor="#263238", linewidth=0.8)
    ax.set_xticks(list(positions))
    ax.set_xticklabels(labels, rotation=25, ha="right")
    ax.set_ylim(0, max(1.0, max(values) * 1.15 if values else 1.0))
    ax.set_ylabel(ylabel)
    ax.set_title(title)
    ax.grid(axis="y", alpha=0.25)
    fig.tight_layout()
    fig.savefig(path)
    plt.close(fig)


