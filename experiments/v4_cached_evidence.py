def run_v3_generator() -> None:
    script = ROOT / "scripts" / "prepare_v3_evidence.py"
    spec = importlib.util.spec_from_file_location("prepare_v3_evidence", script)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"could not import {script}")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    module.main()


def read_csv(rel: str) -> list[dict[str, str]]:
    with (RESULTS / rel).open(newline="", encoding="utf-8-sig") as handle:
        return list(csv.DictReader(handle))


def read_json(rel: str) -> dict:
    return json.loads((RESULTS / rel).read_text(encoding="utf-8"))


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, object]], fields: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def fnum(value: object, digits: int = 3) -> str:
    try:
        return f"{float(value):.{digits}f}"
    except Exception:
        return str(value)


def as_float(row: dict[str, str], key: str) -> float:
    return float(row[key])


def find_row(rows: list[dict[str, str]], method: str, n: int | None = None) -> dict[str, str]:
    for row in rows:
        if row.get("method") != method:
            continue
        if n is None or int(float(row.get("N", -1))) == n:
            return row
    raise KeyError(f"missing method={method} n={n}")


def tex(value: object) -> str:
    text = str(value)
    repl = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(repl.get(ch, ch) for ch in text)


def longtable(
    caption: str,
    label: str,
    columns: str,
    header: str,
    rows: list[str],
    size: str = r"\scriptsize",
    tabcolsep: str = "2pt",
) -> str:
    body = "\n".join(rows)
    return (
        "% Auto-generated by experiments/v4_cached_evidence.py\n"
        "\\begingroup\n"
        f"{size}\n"
        f"\\setlength{{\\tabcolsep}}{{{tabcolsep}}}\n"
        f"\\begin{{longtable}}{{{columns}}}\n"
        f"\\caption{{{tex(caption)}}}\\label{{{label}}}\\\\\n"
        "\\toprule\n"
        f"{header}\\\\\n"
        "\\midrule\n"
        "\\endfirsthead\n"
        "\\toprule\n"
        f"{header}\\\\\n"
        "\\midrule\n"
        "\\endhead\n"
        f"{body}\n"
        "\\bottomrule\n"
        "\\end{longtable}\n"
        "\\endgroup\n"
    )


